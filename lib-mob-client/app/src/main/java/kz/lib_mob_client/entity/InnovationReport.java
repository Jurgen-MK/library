package kz.lib_mob_client.entity;

public class InnovationReport {
    private String regN;
    private String ntiName;
    private String branchName;
    private String lineName;
    private int quantityRp;
    private int quantityStatus1;

    public String getRegN() {
        return regN;
    }

    public void setRegN(String regN) {
        this.regN = regN;
    }

    public String getNtiName() {
        return ntiName;
    }

    public void setNtiName(String ntiName) {
        this.ntiName = ntiName;
    }

    public String getBranchName() {
        return branchName;
    }

    public void setBranchName(String branchName) {
        this.branchName = branchName;
    }

    public String getLineName() {
        return lineName;
    }

    public void setLineName(String lineName) {
        this.lineName = lineName;
    }

    public int getQuantityRp() {
        return quantityRp;
    }

    public void setQuantityRp(int quantityRp) {
        this.quantityRp = quantityRp;
    }

    public int getQuantityStatus1() {
        return quantityStatus1;
    }

    public void setQuantityStatus1(int quantityStatus1) {
        this.quantityStatus1 = quantityStatus1;
    }
}
